    private void btnReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservarActionPerformed
        int asientoSeleccionado = obtenerAsientoSeleccionado(); 
        boolean asientoActualizado = InterfazBase.actualizarAsiento(idavion, asientoSeleccionado, true);
        boolean reservaRegistrada = InterfazBase.registrarReserva(idC, idvuelo);
        if (asientoSeleccionado != -1 && asientoActualizado && reservaRegistrada) {
            JOptionPane.showMessageDialog(null, "Reserva exitosa");
            VistaVuelos.tblViajes.setModel(InterfazBase.extraerViajes());
            Correo.enviarPDF(InterfazBase.obtenerCorreoCliente(idC),idvuelo);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Error en la reserva");
        }
    }//GEN-LAST:event_btnReservarActionPerformed

