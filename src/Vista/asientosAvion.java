    private void desmarcarAsientos(int asientoSeleccionado) {
        for (int i = 0; i < asientosBase.length; i++) {
            if (i != asientoSeleccionado) {
                if(!asientosBase[i]){
                    asientosBase[i] = false;
                    asientosLabels[i].setBackground(Color.WHITE);
                }
            }
        }
    }
    
    public void mostrarAsientos() {
        if (asientosBase == null || asientosLabels == null) {
            throw new NullPointerException("Los arrays de asientos o labels son null.");
        }
        InterfazBase.bloquearAsiento(asientosBase, asientosLabels);
    }
    
    private int obtenerAsientoSeleccionado() {
        for (int i = 0; i < asientosLabels.length; i++) {
            if (asientosLabels[i].getBackground().equals(Color.DARK_GRAY)) {
                return i + 1; 
            }
        }
        return -1; 
    }
    
    public void manejarClickAsiento(int index, JLabel lblAsiento) {
        if (!asientosBase[index]) {
            asiento = index + 1;
            asientosBase[index] = true;
            lblAsiento.setBackground(Color.DARK_GRAY);
            desmarcarAsientos(index);
        } else {
            JOptionPane.showMessageDialog(null, "Elija un asiento disponible");
        }
    }
    
